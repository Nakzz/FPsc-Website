                    <h4>Module 1: Use of the albino mutant allele to explore dominance relationships between wt and mutant alleles, Mendel’s law of segregation, and genetic change over time (evolution)</h4>
                    <p>The albino (alb) FPsc mutant allele is a simple recessive which, in homozygous configuration, has a distinctive yellow/pale green and slow growing phenotype (Figure 1a). alb mutants have a remarkable capacity for persistence; in contrast to canonical albino mutants that germinate, wither and quickly die, alb segregants in an F2 population survive throughout the time it takes wt sibling segregants to complete the plant life cycle, although alb mutants will not flower or set seed under classroom conditions (Figure 1b). In addition to the opportunity to explore Mendelian principles of inheritance, students can be challenged to predict the decline in alb allele frequency through successive generations in a (hypothetical) population (Figure 1c). This scenario represents an extreme example of the means by which deleterious alleles are removed from a population by natural selection—through straightforward Mendelian genetic mechanisms-- but it is a relatively short intellectual leap for for students to appreciate (in less extreme situations) that the frequency of any allelic variant whose effects on reproductive capacity confer a relative fitness &lt; 1.00 will be reduced over time in the gene pool of a population.</p>
