                      <p>In addition to illustrating fundamental evolutionary principles (mere survival is not enough, reproductive potential is the ultimate measure of evolutionary fitness), the persistence of alb mutant segregants throughout the course of the experiment in genetic analysis will hopefully lead students to ponder the molecular basis of the alb mutant phenotype: What’s gone wrong in these plants? Ambitious educators may opt to engage their students in an experimental initiative that we refer to as the “Albino Rescue Project”. The gist of the initiative is that addition of certain compounds—sucrose (the primary product of photosynthesis), amino acids, vitamins, etc.-- directly to plants or to the growing medium may enhance the ability of alb mutant segregants to produce flowers that are able to pass gametes to the next generation.</p>
                    <p>Our lab group at the UW is continuing with efforts to localize the alb mutant locus within the B. rapa genome and have obtained tantalizing results based on gene models that suggest candidate compounds that may be of use to promote flowering, but to date we have identified none whose presence in the growth medium successfully “rescues” the infertility phenotype of alb mutant segregants. We would like nothing better than to have legions of teachers and students to help us in this program.<br />
