                    <h2>R500</h2>
                    <p>R500 is a strain of <em>B. rapa</em> grown primarily in India for the oil in its seeds. It is a naturally-selfing variety, and has developed significant uniformity in its genome.</p>
                    <p>R500 is used as an outcross for FPsc. The F1 generation resulting from the R500xFPsc cross will be heterozygous at all loci. The F2 generation, produced from a self-pollination of the F1 generation, will have a mixture of genes from FPsc and R500 that may be homozygous or heterozygous at any loci (however, the genetic makeup of the F2 generation is still 50 percent FPsc and 50 percent R500).</p>
                    <p>When genetic material from the FPsc x R500 F2 generation is subjected to PCR and gel electrophoresis, a mixture of bands associated with FPsc and R500 will be present.</p>
