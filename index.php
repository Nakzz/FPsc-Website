							<h2>Planting Seeds</h2>
							<p><em>Germs of ideas that emerge from discussions and from other considerations of how to make best use of FPsc genetic resources, or new initiatives under development but “not ready for prime time.”  We especially welcome contributions from visitors/users of FPsc resources and if you have an idea that you’d like to share please email it to <a href="mailto:swoody@wisc.edu" target="_blank">swoody@wisc.edu</a>.</em></p>
							<p><strong>The Mating Game</strong> is an active-learning exercise in which students draw pairs of tokens meant to represent allelic alternatives in the gene pool of a species to model Mendel’s laws of segregation and independent assortment. Playing boards used to implement the exercise, a teachers’ implementation guide, and a problem sheet that may be useful to guide students’ exploration of these fundamental genetic principles are <a href="Lesson_Plans/index.shtm">available here</a>.</p>
							<p>Development continues apace on a <strong>molecular mapping simulation</strong> “app.”  Working with programmers from the UW-Madison Academic Technology department, the sim will enable students to conduct virtual PCRs and gel analyses to map mutant loci within the FPsc genome.</p>
