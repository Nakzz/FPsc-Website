							<p><a href="https://wsst.wildapricot.org/Default.aspx?pageId=1646930"></a><strong>FPsc at WSST, NSTA:</strong> Two FPsc outreach events are in the offing: FPsc plant genetic resources and suggestions for classroom use will be on offer in the University of Wisconsin-Madison Science Alliance booth in the exhibition hall at the <a href="https://wsst.wildapricot.org/Default.aspx?pageId=1646930" target="_blank">Wisconsin Society of Science Teachers (WSST) annual meeting</a> in Appleton, WI March 12-16. FPsc resources will also be presented in the American Society of Plant Biologists (ASPB) booth at the <a href="http://www.nsta.org/conferences/national.aspx" target="_blank">National Science Teachers Association (NSTA) annual meeting</a> in Boston, MA, April 2-6. Program Director Scott Woody will host the FPsc station in the booth at NSTA and will also conduct a workshop (#2156) on Thursday, April 3, 8-9 a.m. entitled “Genetics Education in the Age of Genomics”. Please stop by to say hello at either event, you may just walk away with some free FPsc seeds and new ideas about how to more effectively teach genetics to your students.</p>
							<p><strong>Public release of the FPsc genome sequence:</strong> The <a href="http://www.jgi.doe.gov/" target="_blank">Joint Genome Institute (JGI</a>), the DNA sequencing agency arm of the US Department of Energy (DOE) released the whole genome sequence (WGS) of the FPsc wild type variety in February, 2014 on their web portal <a href="(http://www.phytozome.net/brassica_rapa_FPsc_er.php)" target="_blank">Phytozome</a>. Click on the link and you can browse the FPsc genome, conduct BLAST searches, or just revel in the fact that this amazing resource—a more-or-less complete description of each base-pair that comprises the ~550,000,000 (that’s 550 Mbp) genome of our B. rapa educational model system is available to aid in student education in areas of genetics, evolution, and genomic sciences.</p>
							<p><strong>Genetic Analogies paper now available in the November, 2013 “Genetics Emphasis” edition of <a href="http://www.nabt.org">American Biology Teacher</a>:</strong> We are excited to announce that a manuscript, “Understanding & Teaching Genetics Using Analogies,” coathored by Scott Woody and Ed Himelblau, is in the present edition of ABT.  A pdf version of the article and downloadable images that bring the analogies to life are available <a href="/publications/analogies.shtm">here</a>.</p>


